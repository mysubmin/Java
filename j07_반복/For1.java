package j07_반복;

public class For1 {

	public static void main(String[] args) {
		for(int i = 0 ; i < 10 ; i++) {
			System.out.println(i);
		}
		/*
		 * <for문 기본형태>
		 * for(초기치; 조건문; 증가치;){
		 * 		.....
		 * }
		 */
		int i = 0;
		while(i < 10){
			System.out.println(i);
			i++;
		}
	}

}
